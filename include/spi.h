/* ============================================================================
 * ERROR CODES
 * ========================================================================== */

/**
 * @brief SPI operation error codes
 *
 * All API functions return spi_err_t. Always check the return value.
 */
typedef enum {
    SPI_OK              =  0,   /**< Success */
    SPI_ERR_INVALID_ARG = -1,   /**< NULL pointer or bad argument */
    SPI_ERR_NOT_INIT    = -2,   /**< spi_init() not called */
    SPI_ERR_BUS_BUSY    = -3,   /**< Another transfer in progress */
    SPI_ERR_TIMEOUT     = -4,   /**< Transfer timed out */
    SPI_ERR_OVERFLOW    = -5,   /**< Transfer length exceeds buffer */
    SPI_ERR_PLATFORM    = -6,   /**< Platform HAL error */
    SPI_ERR_NO_DEVICE   = -7,   /**< Device handle not registered */
    SPI_ERR_DMA         = -8,   /**< DMA error */
    SPI_ERR_MODE        = -9,   /**< Unsupported clock mode */
} spi_err_t;

/* ============================================================================
 * ENUMERATIONS
 * ========================================================================== */

/**
 * @brief SPI clock mode (CPOL + CPHA combined)
 *
 * CRITICAL: Every SPI device requires a specific mode.
 * Using the wrong mode produces garbled data with no error indication.
 * Always check the device datasheet "SPI timing" or "CPOL/CPHA" section.
 *
 *  Mode 0 (CPOL=0, CPHA=0): SCK idle LOW,  sample on rising  edge — MOST COMMON
 *  Mode 1 (CPOL=0, CPHA=1): SCK idle LOW,  sample on falling edge
 *  Mode 2 (CPOL=1, CPHA=0): SCK idle HIGH, sample on falling edge
 *  Mode 3 (CPOL=1, CPHA=1): SCK idle HIGH, sample on rising  edge
 */
typedef enum {
    SPI_MODE_0 = 0,     /**< CPOL=0 CPHA=0 — W25Q128, MCP3204, SD card */
    SPI_MODE_1 = 1,     /**< CPOL=0 CPHA=1 — MAX31855, MAX31865 */
    SPI_MODE_2 = 2,     /**< CPOL=1 CPHA=0 — rare */
    SPI_MODE_3 = 3,     /**< CPOL=1 CPHA=1 — ST7735, ST7789, ILI9341 */
} spi_mode_t;

/**
 * @brief Bit transmission order
 */
typedef enum {
    SPI_BITORDER_MSB_FIRST = 0,     /**< MSB sent first — standard (most devices) */
    SPI_BITORDER_LSB_FIRST = 1,     /**< LSB sent first — some shift registers */
} spi_bitorder_t;

/**
 * @brief CS (Chip Select) polarity
 */
typedef enum {
    SPI_CS_ACTIVE_LOW  = 0,     /**< CS LOW = selected (standard) */
    SPI_CS_ACTIVE_HIGH = 1,     /**< CS HIGH = selected (rare, some DACs) */
} spi_cs_polarity_t;

/**
 * @brief Driver type
 */
typedef enum {
    SPI_MODE_HARDWARE = 0,      /**< Hardware SPI peripheral */
    SPI_MODE_SOFTWARE = 1,      /**< Software bit-bang on GPIO */
} spi_driver_t;

/**
 * @brief Bus state
 */
typedef enum {
    SPI_STATE_IDLE      = 0,
    SPI_STATE_BUSY      = 1,
    SPI_STATE_ERROR     = 2,
} spi_state_t;

/* ============================================================================
 * STRUCTURES
 * ========================================================================== */

/**
 * @brief Platform HAL callbacks
 *
 * Implement these 6 functions for your target platform, then pass
 * a pointer to this struct in spi_config_t.
 *
 * For hardware SPI, only cs_write + millis are needed (the peripheral
 * handles MOSI/MISO/SCK automatically).
 */
typedef struct {
    /** Set MOSI pin level (software mode only) */
    void     (*mosi_write)(bool level, void *ctx);

    /** Read MISO pin level (software mode only) */
    bool     (*miso_read)(void *ctx);

    /** Set SCK pin level (software mode only) */
    void     (*sck_write)(bool level, void *ctx);

    /**
     * @brief Set CS pin level
     * @param cs_id   Which CS pin (0 = first device, 1 = second, etc.)
     * @param level   true = HIGH, false = LOW
     * @param ctx     User context
     *
     * NOTE: This is called for EVERY device — use cs_id to select the
     * correct GPIO pin for multi-device buses.
     */
    void     (*cs_write)(uint8_t cs_id, bool level, void *ctx);

    /** Delay in microseconds */
    void     (*delay_us)(uint32_t us);

    /** Millisecond monotonic timestamp (for timeouts) */
    uint32_t (*millis)(void);

    /** User context pointer passed to all callbacks */
    void *user_ctx;
} spi_hal_t;

/**
 * @brief Per-device configuration
 *
 * Each device on the bus has its own config — different clock speed,
 * SPI mode, CS pin, and bit order are all supported simultaneously.
 *
 * @example
 * @code
 * spi_device_cfg_t flash_cfg = {
 *     .cs_id      = 0,
 *     .mode       = SPI_MODE_0,
 *     .speed_hz   = 40000000,   // 40 MHz
 *     .bit_order  = SPI_BITORDER_MSB_FIRST,
 *     .cs_pol     = SPI_CS_ACTIVE_LOW,
 *     .cs_setup_ns = 100,       // 100ns CS-to-SCK setup time
 *     .cs_hold_ns  = 100,       // 100ns SCK-to-CS hold time
 * };
 * @endcode
 */
typedef struct {
    uint8_t           cs_id;        /**< CS pin identifier (passed to hal.cs_write) */
    spi_mode_t        mode;         /**< Clock mode (CPOL/CPHA) */
    uint32_t          speed_hz;     /**< Clock frequency in Hz */
    spi_bitorder_t    bit_order;    /**< MSB or LSB first */
    spi_cs_polarity_t cs_pol;       /**< CS active LOW (default) or HIGH */
    uint32_t          cs_setup_ns;  /**< CS assert-to-SCK delay (nanoseconds) */
    uint32_t          cs_hold_ns;   /**< Last SCK-to-CS-deassert delay (ns) */
} spi_device_cfg_t;

/**
 * @brief Bus-level configuration
 */
typedef struct {
    spi_driver_t    driver;         /**< Hardware or software */
    uint8_t         hw_instance;    /**< Hardware SPI index (0=SPI1, 1=SPI2...) */
    uint32_t        timeout_ms;     /**< Default timeout, 0 = use default */
    spi_hal_t      *hal;            /**< HAL callbacks (required) */
} spi_config_t;

/**
 * @brief Transfer statistics
 */
typedef struct {
    uint32_t tx_bytes;          /**< Total bytes sent */
    uint32_t rx_bytes;          /**< Total bytes received */
    uint32_t transfers;         /**< Completed transfers */
    uint32_t errors;            /**< Error count */
    uint32_t timeouts;          /**< Timeout count */
    uint32_t cs_toggles;        /**< CS assert/deassert cycles */
} spi_stats_t;

/**
 * @brief Transfer descriptor — separates "what" from "how"
 *
 * Improvement over i2c_lib: having a descriptor struct makes it trivial
 * to add DMA support later — just pass the descriptor to the DMA engine.
 */
typedef struct {
    const uint8_t  *tx_buf;     /**< Data to transmit (NULL = send 0x00) */
    uint8_t        *rx_buf;     /**< Buffer for received data (NULL = discard) */
    size_t          len;        /**< Number of bytes to transfer */
} spi_xfer_t;

/**
 * @brief SPI bus handle (opaque)
 */
typedef struct spi_bus_s *spi_bus_t;

/**
 * @brief SPI device handle (opaque)
 *
 * Improvement over i2c_lib: devices are registered on the bus,
 * storing their config. Bus reconfigures automatically on device switch.
 */
typedef struct spi_device_s *spi_device_t;

/* ============================================================================
 * BUS INITIALIZATION
 * ========================================================================== */

/**
 * @brief Initialize an SPI bus
 *
 * @param[out] bus  Pointer to receive bus handle
 * @param[in]  cfg  Bus configuration
 * @return SPI_OK on success
 *
 * @example
 * @code
 * spi_bus_t bus;
 * spi_config_t cfg = {
 *     .driver     = SPI_MODE_SOFTWARE,
 *     .timeout_ms = 50,
 *     .hal        = &my_hal,
 * };
 * spi_init(&bus, &cfg);
 * @endcode
 */
spi_err_t spi_init(spi_bus_t *bus, const spi_config_t *cfg);

/**
 * @brief De-initialize an SPI bus and release all resources
 *
 * @param bus  Bus handle
 * @return SPI_OK on success
 */
spi_err_t spi_deinit(spi_bus_t bus);

/* ============================================================================
 * DEVICE REGISTRATION
 * ========================================================================== */

/**
 * @brief Register a device on the bus
 *
 * IMPROVEMENT OVER i2c_lib: Devices are registered with their own config.
 * The bus automatically switches mode/speed when a different device is selected.
 * No manual reconfiguration needed between device transfers.
 *
 * @param bus          Bus handle
 * @param[in]  dev_cfg Device-specific configuration
 * @param[out] device  Pointer to receive device handle
 * @return SPI_OK on success
 *
 * @example
 * @code
 * spi_device_t flash;
 * spi_device_cfg_t cfg = {
 *     .cs_id    = 0,
 *     .mode     = SPI_MODE_0,
 *     .speed_hz = 40000000,
 * };
 * spi_register_device(bus, &cfg, &flash);
 * @endcode
 */
spi_err_t spi_register_device(spi_bus_t bus, const spi_device_cfg_t *dev_cfg,
                               spi_device_t *device);

/* ============================================================================
 * TRANSACTION CONTROL
 *
 * IMPROVEMENT OVER i2c_lib: Explicit begin/end transaction functions
 * make CS control clear and prevent half-open transactions on error.
 * ========================================================================== */

/**
 * @brief Assert CS and begin a transaction with a device
 *
 * Pulls CS LOW (or HIGH if cs_pol = ACTIVE_HIGH), applies cs_setup_ns delay.
 * All transfers between begin and end share the same CS assertion.
 *
 * @param device  Device handle
 * @return SPI_OK on success
 */
spi_err_t spi_transaction_begin(spi_device_t device);

/**
 * @brief End transaction and deassert CS
 *
 * Applies cs_hold_ns delay, then releases CS.
 * Always call this — even on error paths — to prevent bus lockup.
 *
 * @param device  Device handle
 * @return SPI_OK on success
 */
spi_err_t spi_transaction_end(spi_device_t device);

/* ============================================================================
 * CORE TRANSFER FUNCTIONS
 * ========================================================================== */

/**
 * @brief Full-duplex transfer: send and receive simultaneously
 *
 * The fundamental SPI operation. For every byte sent on MOSI, one byte
 * is received on MISO at exactly the same time.
 *
 * Must be called between spi_transaction_begin() and spi_transaction_end().
 *
 * @param device  Device handle
 * @param xfer    Transfer descriptor (tx_buf, rx_buf, len)
 * @return SPI_OK on success
 *
 * @example
 * @code
 * uint8_t tx[] = { 0x03, 0x00, 0x10, 0x00 };  // READ command + 24-bit addr
 * uint8_t rx[4];
 * spi_xfer_t xfer = { .tx_buf = tx, .rx_buf = rx, .len = 4 };
 * spi_transaction_begin(flash);
 * spi_transfer(flash, &xfer);
 * spi_transaction_end(flash);
 * @endcode
 */
spi_err_t spi_transfer(spi_device_t device, const spi_xfer_t *xfer);

/**
 * @brief Transmit only — discard received bytes
 *
 * Convenience wrapper: sends data and ignores MISO.
 * Used for commands, display pixel data, register writes.
 *
 * @param device  Device handle
 * @param data    Buffer to transmit
 * @param len     Number of bytes
 * @return SPI_OK on success
 */
spi_err_t spi_write(spi_device_t device, const uint8_t *data, size_t len);

/**
 * @brief Receive only — transmit 0x00 bytes on MOSI
 *
 * Convenience wrapper: clocks out dummy bytes to receive from slave.
 * Used for ADC reads, thermocouple reads, status register reads.
 *
 * @param device  Device handle
 * @param data    Buffer to store received bytes
 * @param len     Number of bytes to receive
 * @return SPI_OK on success
 */
spi_err_t spi_read(spi_device_t device, uint8_t *data, size_t len);

/* ============================================================================
 * TYPED TRANSFER FUNCTIONS
 * IMPROVEMENT OVER i2c_lib: typed functions for common word sizes.
 * Eliminates manual byte-packing/unpacking in driver code.
 * ========================================================================== */

/**
 * @brief Transfer a single byte and return the received byte
 *
 * @param device    Device handle
 * @param tx_byte   Byte to send
 * @param[out] rx   Received byte (NULL to discard)
 * @return SPI_OK on success
 *
 * @example
 * @code
 * // Read W25Q128 status register
 * uint8_t status;
 * spi_transaction_begin(flash);
 * spi_transfer8(flash, 0x05, NULL);   // send RDSR command
 * spi_transfer8(flash, 0x00, &status); // read response
 * spi_transaction_end(flash);
 * @endcode
 */
spi_err_t spi_transfer8(spi_device_t device, uint8_t tx_byte, uint8_t *rx);

/**
 * @brief Transfer a 16-bit word (big-endian on wire)
 *
 * Sends MSB first. Commonly used for 12-bit ADC reads (MCP3204)
 * and 16-bit display commands (ST7735).
 *
 * @param device    Device handle
 * @param tx_word   16-bit value to send
 * @param[out] rx   Received 16-bit value (NULL to discard)
 * @return SPI_OK on success
 */
spi_err_t spi_transfer16(spi_device_t device, uint16_t tx_word, uint16_t *rx);

/**
 * @brief Transfer a 32-bit word (big-endian on wire)
 *
 * Used for thermocouple reads (MAX31855 returns 32-bit frame),
 * and 32-bit flash operations.
 *
 * @param device    Device handle
 * @param tx_word   32-bit value to send
 * @param[out] rx   Received 32-bit value (NULL to discard)
 * @return SPI_OK on success
 */
spi_err_t spi_transfer32(spi_device_t device, uint32_t tx_word, uint32_t *rx);

/* ============================================================================
 * COMMAND + DATA HELPERS
 * Commonly used for displays and flash memory
 * ========================================================================== */

/**
 * @brief Send a command byte followed by data bytes (no CS release between)
 *
 * Sends command and data in a single CS assertion:
 *   CS LOW → cmd byte → data bytes → CS HIGH
 *
 * Used heavily by: display controllers (ST7735, ILI9341),
 *                  flash memory (W25Q128 page program)
 *
 * @param device    Device handle
 * @param cmd       Command byte
 * @param data      Data payload (NULL if len=0)
 * @param len       Number of data bytes after command
 * @return SPI_OK on success
 *
 * @example
 * @code
 * // ST7735: set column address range
 * uint8_t col_data[] = { 0x00, 0x00, 0x00, 0x7F };  // start=0, end=127
 * spi_write_cmd(display, 0x2A, col_data, 4);
 * @endcode
 */
spi_err_t spi_write_cmd(spi_device_t device, uint8_t cmd,
                         const uint8_t *data, size_t len);

/**
 * @brief Send a command byte and receive response bytes
 *
 * CS LOW → cmd byte → receive data bytes → CS HIGH
 *
 * Used by: flash memory (read ID, read data), ADCs (read conversion)
 *
 * @param device    Device handle
 * @param cmd       Command byte to send
 * @param[out] data Buffer for received bytes
 * @param len       Number of bytes to receive
 * @return SPI_OK on success
 *
 * @example
 * @code
 * // W25Q128: read JEDEC ID (3 bytes: manufacturer + device ID)
 * uint8_t id[3];
 * spi_read_cmd(flash, 0x9F, id, 3);
 * // id[0] = 0xEF (Winbond), id[1] = 0x40, id[2] = 0x18
 * @endcode
 */
spi_err_t spi_read_cmd(spi_device_t device, uint8_t cmd,
                        uint8_t *data, size_t len);

