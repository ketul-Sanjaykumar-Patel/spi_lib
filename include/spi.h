/**
 * @file    spi.h
 * @brief   Universal SPI (Serial Peripheral Interface) Driver Library
 * @version 1.0.0
 * @date    2024
 *
 * ============================================================================
 *                        SPI PROTOCOL OVERVIEW
 * ============================================================================
 *
 * SPI (Serial Peripheral Interface) is a synchronous, full-duplex, master-slave
 * serial communication protocol developed by Motorola in the mid-1980s.
 * It is the dominant protocol for high-speed embedded peripheral communication.
 *
 * PHYSICAL BUS — 4 wires:
 *   MOSI : Master Out Slave In   — data FROM master TO slave
 *   MISO : Master In Slave Out   — data FROM slave TO master
 *   SCK  : Serial Clock          — always generated by master
 *   CS   : Chip Select (active LOW) — selects which slave is active
 *
 * Unlike I2C, SPI has NO pull-up resistors and NO addressing.
 * Each slave device needs its own dedicated CS pin on the master.
 *
 *              MASTER
 *           ┌──────────┐
 *      MOSI─┤          ├─MOSI──── SLAVE1
 *      MISO─┤          ├─MISO──── SLAVE1
 *       SCK─┤          ├─SCK───── SLAVE1
 *       CS0─┤          ├─CS────── SLAVE1
 *       CS1─┤          ├──────────────── SLAVE2 CS
 *           └──────────┘
 *
 * FULL-DUPLEX:
 *   Master sends 1 byte on MOSI while simultaneously receiving 1 byte on MISO.
 *   Both happen in the SAME clock cycle. This is a fundamental difference from I2C.
 *
 * ============================================================================
 *                        SPI CLOCK MODES (CPOL / CPHA)
 * ============================================================================
 *
 * Every SPI device has a required clock mode. Using the wrong mode = garbled data.
 * Check the device datasheet for "SPI Mode" or "CPOL/CPHA" in the timing section.
 *
 *  CPOL (Clock Polarity): idle state of SCK
 *    0 = SCK idles LOW
 *    1 = SCK idles HIGH
 *
 *  CPHA (Clock Phase): when data is sampled
 *    0 = data sampled on LEADING  edge (first edge)
 *    1 = data sampled on TRAILING edge (second edge)
 *
 *  ┌──────┬──────┬──────┬───────────────────────────────────────────────┐
 *  │ Mode │ CPOL │ CPHA │ Common devices                                │
 *  ├──────┼──────┼──────┼───────────────────────────────────────────────┤
 *  │  0   │  0   │  0   │ W25Qxx Flash, MCP3204 ADC, SD cards           │
 *  │  1   │  0   │  1   │ MAX31855 thermocouple, some DACs              │
 *  │  2   │  1   │  0   │ Some sensors, rare                            │
 *  │  3   │  1   │  1   │ ST7735 TFT display, some ADCs                 │
 *  └──────┴──────┴──────┴───────────────────────────────────────────────┘
 *
 * MODE 0 timing (most common):
 *   SCK: ____/‾\_/‾\_/‾\_/‾\_/‾\_/‾\_/‾\_/‾\____
 *  MOSI: ═══[b7][b6][b5][b4][b3][b2][b1][b0]═══
 *  MISO: ═══[b7][b6][b5][b4][b3][b2][b1][b0]═══
 *    CS: ‾‾‾\___________________________________/‾
 *           ^ CS LOW (select)        CS HIGH ^ (deselect)
 *
 * ============================================================================
 *                        TRANSACTION STRUCTURE
 * ============================================================================
 *
 *  A typical SPI transaction:
 *    1. Pull CS LOW  (assert chip select)
 *    2. Transfer N bytes (MOSI out, MISO in simultaneously)
 *    3. Pull CS HIGH (deselect — ends transaction)
 *
 *  For devices with command + data structure (most flash, displays):
 *    CS LOW → send command byte → send/recv data bytes → CS HIGH
 *
 *  For read-only devices (thermocouples, some ADCs):
 *    CS LOW → clock out dummy bytes (0x00) → receive data on MISO → CS HIGH
 *
 * ============================================================================
 *                        REAL-WORLD USAGE (2024)
 * ============================================================================
 *
 *  [STORAGE & MEMORY]
 *   - W25Q128 / W25Q256 : NOR Flash (firmware storage, data logging)
 *     → Used in: ESP32 modules, STM32 boards, IoT devices, routers
 *   - AT45DB / S25FL     : DataFlash (high-endurance logging)
 *   - FRAM (MB85RS)      : Ferroelectric RAM (industrial logging, no wear)
 *   - SD / microSD cards : Mass storage (FAT32 over SPI mode)
 *     → Used in: data loggers, cameras, Raspberry Pi boot storage
 *
 *  [SENSORS & MEASUREMENT]
 *   - MAX31855 / MAX31865 : Thermocouple/RTD amplifier (industrial temperature)
 *     → Used in: 3D printers (hotend), industrial ovens, HVAC
 *   - MCP3204 / ADS1256   : High-resolution ADC (precision measurement)
 *     → Used in: oscilloscopes, data acquisition, audio
 *   - ICM-42688-P         : High-end IMU (aerospace, precision drones)
 *   - BMP388              : Barometric pressure (altimeters, weather)
 *   - LIS3DSH             : Accelerometer (ST eval boards, wearables)
 *   - ADXL345             : Accelerometer (tilt sensing, impact detection)
 *
 *  [DISPLAYS]
 *   - ST7735 / ST7789     : Color TFT LCD controller (small embedded displays)
 *     → Used in: smartwatches, handheld instruments, IoT dashboards
 *   - ILI9341 / ILI9488  : 320×240 / 480×320 TFT (mid-size displays)
 *     → Used in: GPS units, medical monitors, industrial HMI
 *   - SSD1351             : OLED color display (high contrast wearables)
 *   - MAX7219             : LED matrix/7-seg driver (scoreboards, clocks)
 *   - e-Paper / e-Ink     : Low-power bistable displays (price tags, badges)
 *
 *  [POWER & CONVERSION]
 *   - MCP4921 / DAC8551   : 12-bit DAC (waveform gen, voltage reference)
 *   - ADE7758             : Energy metering IC (smart power meters)
 *   - LTC2943             : Battery fuel gauge (industrial battery packs)
 *
 *  [WIRELESS & NETWORKING]
 *   - CC1101 / RFM69      : Sub-GHz RF transceiver (LoRa, 433MHz remotes)
 *   - W5500               : Ethernet controller (embedded web servers)
 *   - ENC28J60            : Ethernet (Arduino Ethernet shield)
 *   - nRF24L01            : 2.4GHz RF (RC controllers, wireless sensors)
 *
 *  [AUTOMOTIVE (AEC-Q100)]
 *   - MCP2515             : CAN bus controller (vehicle OBD, industrial CAN)
 *   - TLE5009             : GMR angle sensor (electric power steering)
 *   - TPIC6C595           : Power shift register (automotive lighting)
 *
 *  [CONSUMER ELECTRONICS]
 *   - SPI Flash in every WiFi router (firmware storage)
 *   - SPI in every smartphone (NOR flash for bootloader)
 *   - FPGA configuration via SPI
 *   - SPI in hard drives (internal config flash)
 *
 * ============================================================================
 *                        SPI vs I2C — WHEN TO USE WHICH
 * ============================================================================
 *
 *  Use SPI when:
 *   - Speed matters (SPI: 50+ MHz, I2C: max 3.4 MHz)
 *   - Full-duplex simultaneous TX/RX needed
 *   - Connecting displays or large flash memories
 *   - Long wire runs (SPI has better noise immunity at speed)
 *
 *  Use I2C when:
 *   - Minimizing wire count (only 2 wires vs 4)
 *   - Connecting many low-speed sensors on one bus
 *   - GPIO pins are scarce
 *
 * ============================================================================
 *                        IMPROVEMENTS OVER i2c_lib
 * ============================================================================
 *
 *  This library improves on the i2c_lib design in the following ways:
 *
 *  1. TYPED TRANSFER FUNCTIONS
 *     i2c_lib used raw uint8_t* everywhere.
 *     spi_lib adds spi_transfer8(), spi_transfer16(), spi_transfer32()
 *     for common word sizes — no manual byte-packing needed.
 *
 *  2. DMA-READY STRUCTURE
 *     Transfer descriptors (spi_xfer_t) separate the "what to transfer"
 *     from the "how to transfer" — makes adding DMA trivial later.
 *
 *  3. TRANSACTION OBJECT
 *     spi_transaction_begin() / spi_transaction_end() wrap CS control
 *     explicitly, preventing half-open transactions on error paths.
 *
 *  4. DEVICE HANDLE WITH CONFIG
 *     Each device (spi_device_t) stores its own CS pin, mode, speed,
 *     and bit order. Switching between two devices on the same bus
 *     automatically reconfigures the bus — no manual mode switching.
 *
 *  5. COMPILE-TIME ASSERTIONS
 *     Static asserts on struct sizes and config values catch
 *     misconfiguration at build time, not runtime.
 *
 *  6. CONST-CORRECT API
 *     All read-only pointer parameters are marked const throughout.
 *
 * @author  SPI Library Contributors
 * @license MIT License
 */

