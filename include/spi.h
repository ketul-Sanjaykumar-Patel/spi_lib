/**
 * @brief Convert error code to human-readable string
 *
 * @example
 * @code
 * spi_err_t err = spi_write(device, buf, len);
 * if (err != SPI_OK) printf("SPI error: %s\n", spi_err_to_string(err));
 * @endcode
 */
const char *spi_err_to_string(spi_err_t err);

